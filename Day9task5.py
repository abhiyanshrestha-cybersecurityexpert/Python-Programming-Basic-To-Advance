a = ['Hello', 'World','Python','Nepal', 'Django'] # List of strings
indexing = 0  # indexing is set to zero
while indexing < len(a): # using while loop for calling indexing is less than len List (a)
    print(a[indexing]) # printing output with by calling list (a) with indexing
    indexing += 1 # Indexing is increasing by 1 